function E=ditances(E1,E2,E3)

            figure();
            hold on;
            E=(E1(:,1)-E2(:,1)).^2+(E1(:,2)-E2(:,2)).^2+(E1(:,3)-E2(:,3)).^2;
            
           plot(E);
           pause(2);
           plot((E2(:,1)-E3(:,1)).^2+(E2(:,2)-E3(:,2)).^2+(E2(:,3)-E3(:,3)).^2);
           pause(2);
           plot((E3(:,1)-E2(:,1)).^2+(E3(:,2)-E2(:,2)).^2+(E3(:,3)-E2(:,3)).^2);
            %
    
end

