function encheck(E1,Ev1,S,Sv)

    G=6.67408*10^(-11);
    
    M=1.98855*10^30;
    m=1000;
    
    KE = 0.5*(m*(Ev1(:,1).^2+Ev1(:,2).^2+Ev1(:,3).^2)+M*(Sv(:,1).^2+Sv(:,2).^2+Sv(:,3).^2));
    PE = G*M*m/sqrt((E1(:,1)-S(:,1)).^2+(E1(:,2)-S(:,2)).^2+(E1(:,3)-S(:,3)).^2);
    
    plot(KE+PE','y');

end

