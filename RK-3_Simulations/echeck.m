function echeck(E,Ev,B,Bv)

    G=6.67408*10^(-11);
    
    M=G*1.98855*10^30;
    m=G*1000;
    
    
    KE= 0.5*(m*(Ev(:,1).^2+Ev(:,2).^2+Ev(:,3).^2 ) +M*(Bv(:,1).^2+Bv(:,2).^2+Bv(:,3).^2));
    PE= G*m*M/sqrt((E(:,1)-B(:,1)).^2+(E(:,2)-B(:,2)).^2+(E(:,3)-B(:,3)).^2);
   

    
   plot(KE+PE');


end

