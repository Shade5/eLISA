function m=miniz(E1,E2,E3)

           
            E12=sqrt((E1(:,1)-E2(:,1)).^2+(E1(:,2)-E2(:,2)).^2+(E1(:,3)-E2(:,3)).^2);
            E13=sqrt((E1(:,1)-E3(:,1)).^2+(E1(:,2)-E3(:,2)).^2+(E1(:,3)-E3(:,3)).^2);
            E32=sqrt((E3(:,1)-E2(:,1)).^2+(E3(:,2)-E2(:,2)).^2+(E3(:,3)-E2(:,3)).^2);
            
          m=sum(E12+E13+E32);
    
end

